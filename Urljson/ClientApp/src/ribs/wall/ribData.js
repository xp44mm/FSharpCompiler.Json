export const getRibKind = (ribSpec = '[5') => {
    if (ribSpec.startsWith('['))
        return '槽钢'
    else if (ribSpec.startsWith('I'))
        return '工字钢'
    else if (ribSpec.startsWith('H'))
        return 'H型钢'
    else if (ribSpec.match(/^L\d+×\d+$/))
        return '等边角钢'
    else if (ribSpec.match(/^L\d+×\d+×\d+$/))
        return '不等边角钢'
    else
        throw new RangeError('ribKind输入参数不在范围内：' + ribSpec)
}
